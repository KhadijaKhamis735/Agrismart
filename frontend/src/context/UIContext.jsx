export const UIProvider = ({ children }) => {
  const [language, setLanguageState] = useState(() => {
    const saved = localStorage.getItem(LANGUAGE_KEY) || "en";
    return ALLOWED_LANGUAGES.includes(saved) ? saved : "en";
  });
  const [theme, setThemeState] = useState(() => {
    const initial = getInitialTheme();
    return ALLOWED_THEMES.includes(initial) ? initial : "light";
  });

  const setLanguage = (nextLanguage) => {
    if (ALLOWED_LANGUAGES.includes(nextLanguage)) {
      setLanguageState(nextLanguage);
    }
  };

  const setTheme = (nextTheme) => {
    if (ALLOWED_THEMES.includes(nextTheme)) {
      setThemeState(nextTheme);
    }
  };

  useEffect(() => {
    localStorage.setItem(LANGUAGE_KEY, language);
  }, [language]);

  useEffect(() => {
    localStorage.setItem(THEME_KEY, theme);
    document.documentElement.classList.toggle("dark", theme === "dark");
    document.body.classList.toggle("dark", theme === "dark");
  }, [theme]);

  const toggleTheme = () => {
    setTheme(theme === "dark" ? "light" : "dark");
  };

  const value = useMemo(
    () => ({
      language,
      setLanguage,
      theme,
      setTheme,
      toggleTheme,
      t: (key) => translations[language]?.[key] || translations.en[key] || key,
    }),
    [language, theme]
  );

  return <UIContext.Provider value={value}>{children}</UIContext.Provider>;
};

export const useUI = () => {
  const context = useContext(UIContext);
  if (!context) {
    throw new Error("useUI must be used within UIProvider");
  }
  return context;
};
